// Kiro color palette
export const KIRO_PALETTE = {
  '0': null,        // transparent (background)
  '1': '#FFFFFF',   // white body
  '7': '#000000',   // black eyes
};

// Render sprite to canvas
export function drawSprite(ctx, sprite, palette, bgColor, scale = 2) {
  sprite.forEach((row, y) => {
    [...row].forEach((char, x) => {
      const color = palette[char];
      if (color) {
        ctx.fillStyle = color;
        ctx.fillRect(x * scale, y * scale, scale, scale);
      }
      // '0' or undefined = transparent, skip (background already drawn)
    });
  });
}

// Check if character has sprite
export function hasSprite(characterName) {
  return characterName === 'kiro';
}

// Get sprite and palette for character
export function getSprite(characterName) {
  if (characterName === 'kiro') {
    return { sprite: KIRO_SPRITE, palette: KIRO_PALETTE };
  }
  return null;
}
